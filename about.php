<p>his&hurst is a small business run by Sophie Milton. We create simple, useful & sometimes wonderful things by recycling wood that others throw away.</p>

<p>Sophie Milton moved from London to the South Downs village of Hurstpierpoint in 2013. Once she began to unwind she revisited her love of art and began painting, initially using traditional canvas, then experimenting with recycled wood. </p>

<p>Sophie's art encapsulates the ethos of reduce, reuse and recycle, painting directly onto canvases she builds herself using wood specifically chosen to enhance each picture.
Sophie's work is inspired by travel, most importantly the sea. The work can be hung inside, or outside to create a view.</p>

<p>The work can be viewed at <a href='http://www.brightonmarina.co.uk/shops/the-laughing-dog-gifts-gallery'>The Laughing Dog gallery</a> in Brighton, or you can <a href='contact.php'>contact Sophie</a> directly.</p>

